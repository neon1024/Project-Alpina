$("#em1").on("mouseenter", function() {

    $("li#em1").css("border-bottom-color", "#007DFF");

    $(".em1").css("display", "flex");

});

$(".em1").on("mouseover", function() {

    $(".em1").css("display", "flex");

    $("li#em1").css("border-bottom-color", "#007DFF");

});

$(".em1").on("mouseleave", function() {

    $(".em1").css("display", "none");
    
    $("li#em1").css("border-bottom-color", "transparent");

});

$("#em1").on("mouseleave", function() {

    $("li#em1").css("border-bottom-color", "transparent");

    $(".em1").css("display", "none");

});
