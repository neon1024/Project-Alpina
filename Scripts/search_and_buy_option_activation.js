$("#sab1").on("mouseenter", function() {

    $("li#sab1").css("border-bottom-color", "#007DFF");

    $(".sab1").css("display", "flex");

});

$(".sab1").on("mouseover", function() {

    $(".sab1").css("display", "flex");

    $("li#sab1").css("border-bottom-color", "#007DFF");

});

$(".sab1").on("mouseleave", function() {

    $(".sab1").css("display", "none");
    
    $("li#sab1").css("border-bottom-color", "transparent");

});

$("#sab1").on("mouseleave", function() {

    $("li#sab1").css("border-bottom-color", "transparent");

    $(".sab1").css("display", "none");

});

